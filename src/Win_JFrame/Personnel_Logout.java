    class TimeThread extends Thread {

        @Override
        public void run() {
            while (true) {

                SimpleDateFormat AutoSdFormat = new SimpleDateFormat("yyyy/MM/dd hh:mm:ss");
                Date AutoDate = new Date();
                String AutoStrDate = AutoSdFormat.format(AutoDate);
                jLabel4.setText(AutoStrDate);

                try {
                    Thread.sleep(1000);
                } catch (InterruptedException e) {
                    //return;
                }

            }
        }

    }

